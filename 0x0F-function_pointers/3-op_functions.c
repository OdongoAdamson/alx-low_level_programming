#include "3-calc.h"

/**
 * op_add - addition
 * op_sub - subtracts
 * op_mul - multiplies
 * op_div - divides
 * op_mod - performs modulus
 * @a: first number
 * @b: second number
 *
 * Return : final result or some erro
 */

int op_add(int a, int b)
{
	return (a + b);
	printf("\n");
}

int op_sub(int a, int b)
{
	return (a - b);
	printf("\n");
}

int op_mul(int a, int b)
{
	return (a * b);
	printf("\n");
}

int op_div(int a, int b)
{
	return (a / b);
	printf("\n");
}

int op_mod(int a, int b);
{
	return (a % b);
	printf("\n");
}
